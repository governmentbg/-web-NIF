CREATE TABLE authentication (
  authentication number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  authenticator varchar2(128) NOT NULL,
  settings clob,
  position number NOT NULL,
  disabled number DEFAULT 0 NOT NULL,
  conditions varchar2(2000) DEFAULT '',
  CONSTRAINT PK_AUTHENTICATION PRIMARY KEY (authentication)
);


CREATE TABLE uploads (
  id number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  name clob DEFAULT '',
  location clob DEFAULT '',
  bytesize number DEFAULT 0 NOT NULL ,
  uploaded timestamp DEFAULT SYSTIMESTAMP NOT NULL ,
  hash varchar2(32) DEFAULT '' ,
  data clob DEFAULT '',
  settings clob DEFAULT '',
  CONSTRAINT PK_UPLOADS PRIMARY KEY (id)
);

CREATE TABLE users (
  usr number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  name varchar2(80) DEFAULT '' ,
  mail varchar2(255) DEFAULT '' ,
  tfa number DEFAULT 0 NOT NULL ,
  disabled number DEFAULT 0  NOT NULL ,
  avatar number  DEFAULT NULL,
  avatar_data clob DEFAULT '',
  data clob DEFAULT '',
  push clob DEFAULT '',
  sessions clob DEFAULT '',
  CONSTRAINT PK_USERS PRIMARY KEY (usr),
  CONSTRAINT FK_USER_AVATAR FOREIGN KEY (avatar) REFERENCES uploads (id)
);

CREATE TABLE permissions (
  perm varchar2(255)  NOT NULL ,
  created timestamp NOT NULL ,
  CONSTRAINT PK_PERMISSIONS PRIMARY KEY (perm)
);

CREATE TABLE grps (
  grp number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  name varchar2(255) NOT NULL ,
  created timestamp NOT NULL ,
  CONSTRAINT PK_GRPS PRIMARY KEY (grp)
);


