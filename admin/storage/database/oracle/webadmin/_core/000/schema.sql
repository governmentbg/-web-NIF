CREATE TABLE organization (
  org number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  lft number  NOT NULL ,
  rgt number  NOT NULL ,
  lvl number  NOT NULL ,
  pid number  DEFAULT NULL ,
  pos number  NOT NULL ,
  title varchar2(50) DEFAULT '' ,
  properties clob DEFAULT '' ,
  CONSTRAINT PK_ORGANIZATION PRIMARY KEY (org),
  CONSTRAINT FK_ORGANIZATION_PARENT FOREIGN KEY (pid) REFERENCES organization (org) ON DELETE CASCADE 
);

CREATE TABLE log (
  id number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  created timestamp DEFAULT SYSTIMESTAMP NOT NULL ,
  lvl varchar2(20) DEFAULT 'error' NOT NULL CHECK(lvl IN ('emergency','alert','critical','error','warning','notice','info','debug')),
  message varchar2(255) DEFAULT '' ,
  context clob DEFAULT '',
  request clob DEFAULT '',
  response clob DEFAULT '',
  ip varchar2(45) DEFAULT '' ,
  usr number  DEFAULT NULL ,
  usr_name varchar2(80) DEFAULT NULL ,
  CONSTRAINT PK_LOG PRIMARY KEY (id),
  CONSTRAINT FK_LOG_USER FOREIGN KEY (usr) REFERENCES users (usr)
);

CREATE TABLE mails (
  mail number GENERATED BY DEFAULT ON NULL as IDENTITY(START with 1 INCREMENT by 1),
  recipient varchar2(1000) NOT NULL,
  subject varchar2(2000)  DEFAULT '',
  content clob DEFAULT '',
  priority number DEFAULT 0,
  added timestamp NOT NULL ,
  started timestamp DEFAULT NULL,
  finished timestamp DEFAULT NULL,
  result varchar2(255) DEFAULT '',
  CONSTRAINT PK_MAILS PRIMARY KEY (mail)
);

